# 集合的定义：大括号包含元素，每个元素用逗号隔开
#与字典的区别：字典键值对一一对应关系，集合是没有的
# 举例
# s1={10,20,30}
# print(type(s1))    #<class 'set'>
# print(s1)          #{10, 20, 30}
#
# # 定义：用set()
# s2=set('asd')
# print(type(s2))   #<class 'set'>
# print(s2)         #{'a', 'd', 's'}
#
# # 定义一个空集合
# s3={}    #系统认为这是定义一个空字典
# print(type(s3))  #<class 'dict'>
#
# s4=set()      #定义一个空集合
# print(type(s4))   #<class 'set'>

# 集合的特性：无序不重复的数据类型，不支持下标操作

