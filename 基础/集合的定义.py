# 集合的常见操作
# 一.增
# 1.add
# s1={10,20}
# s1.add(100)    #{100, 10, 20}
# print(s1)
#
# s1.add(10)
# print(s1)    #{100, 10, 20}    自动去重

# 2.update()  追加的数据必须是可迭代的对象（列表，字典，集合，元组）
# s1={10,20}
# s1.update(100)
# print(s1)    #'int' object is not iterable  整形对象不是可迭代对象

# # 添加列表
# s1={10,20}
# s1.update([100])    #[100] 类型为列表，是一个可迭代对象
# print(s1)          #{100, 10, 20}
#
# #添加元组
# s1={10,20}
# s1.update((22,11))
# print(s1)
#
# #添加集合
# s1={10,20}
# s1.update({100})
# print(s1)
#
# # 添加字典
# s1={10,20}
# s1.update({22:33})   #只添加了key
