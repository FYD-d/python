# 一.函数
# 定义：把具有独立功能的代码块组合成一个个小模块
# 作用：提高写代码的效率，实现代码复用————流程标准化
# 为什么要有函数：可以在不同的地方多次调用，想要使用几次就使用几次，比for.while循环更加灵活。只需要调用，不需要重新写

# 函数格式：
# def 函数名():
#     函数体

# 函数名需要符合标志符的命名规范:必须是字母 下划线 数字且不能以数字开头。 不能与关键字重名  。见名知其意  。  函数名一
#                          见名知其意  。  函数名一律小写，用下划线隔开


